#!/usr/bin/env python3
"""build/merge.py -- concatenates Ess's per-namespace src/*.lua files into one deployable dist/Ess.lua.

Two things this script does that a naive `cat src/*.lua` would get wrong:

1. EXPLICIT ordered manifest, not an alphabetical glob. Within a tiered namespace (planned: e.g.
   `31_mark_raw.lua` / `31_mark.lua` / `31_mark_easy.lua`), alphabetical filename order gets the
   Raw -> Core -> Easy dependency order BACKWARDS (`_mark.lua` < `_mark_easy.lua` < `_mark_raw.lua`
   alphabetically, but Raw must load first). See FEATURE_SHEET.md's "Repo / build architecture" section.

2. Each file's contents are wrapped in its own `do ... end` block. Concatenating plain Lua text into one
   chunk means every source file shares ONE lexical scope -- a top-level `local` in one file would
   otherwise leak into (and could collide with) every file merged after it. Wrapping each file's contents
   in `do...end` scopes its top-level locals to itself, matching what "per-namespace file" is supposed to
   mean, while its PUBLIC API (always a field write like `function Ess.Player.character(i)`) still ends up
   on the one shared `_G.Ess` table regardless -- `do...end` only affects *locals*, and functions defined
   inside it still close over whatever they need from other files (which is fine: by the time any of
   THOSE functions is actually CALLED, the whole merged chunk has already finished loading top to bottom).

Usage: `python build/merge.py`, run from anywhere -- paths below are relative to this file's own location.
"""
import datetime
import pathlib
import sys

ROOT = pathlib.Path(__file__).resolve().parent.parent
SRC = ROOT / "src"
DIST = ROOT / "dist"

# The build order. This list IS the dependency order -- add new files here as they're written, in the
# position their dependencies require, not just appended at the end.
MANIFEST = [
    "00_core.lua",
    "10_player.lua",
    "11_object.lua",
    "12_vehicle.lua",
    "13_probe.lua",
    "20_loop.lua",
    "21_input.lua",
    "22_state.lua",
    "30_track.lua",
    "31_mark_raw.lua",
    "31_mark.lua",
    "31_mark_easy.lua",
    "40_gfx.lua",
    "50_bones.lua",
    "51_camera.lua",
    "52_points.lua",
    "53_rng.lua",
    "90_override.lua",
    "60_aiorders_raw.lua",
    "60_aiorders.lua",
    "60_aiorders_easy.lua",
    "61_relations_raw.lua",
    "61_relations.lua",
    "61_relations_easy.lua",
    "62_triggers_raw.lua",
    "62_triggers.lua",
    "62_triggers_easy.lua",
    "63_sandbox_raw.lua",
    "63_sandbox.lua",
    "63_sandbox_easy.lua",
    "99_adopt.lua",
]


def main():
    missing = [f for f in MANIFEST if not (SRC / f).exists()]
    if missing:
        print("[FAIL] manifest lists files that don't exist in src/: " + ", ".join(missing))
        sys.exit(1)

    have = {f.name for f in SRC.glob("*.lua")}
    orphans = sorted(have - set(MANIFEST))
    if orphans:
        print("[WARN] src/ has .lua files NOT in the manifest (won't be built in): " + ", ".join(orphans))

    parts = [
        "-- Ess.lua -- GENERATED by build/merge.py, do not hand-edit (edit src/*.lua and re-run it).",
        "-- Built %s from: %s" % (
            datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            ", ".join(MANIFEST),
        ),
        "",
    ]
    for name in MANIFEST:
        banner = "-- ==== " + name + " " + "=" * max(1, 60 - len(name))
        body = (SRC / name).read_text(encoding="utf-8").rstrip()
        parts.append(banner)
        parts.append("do")
        parts.append(body)
        parts.append("end")
        parts.append("")

    parts.append("-- ==== boot " + "=" * 55)
    parts.append('Ess.Log("v" .. Ess.VERSION .. " ready")')
    parts.append("")

    DIST.mkdir(exist_ok=True)
    out_path = DIST / "Ess.lua"
    out_path.write_text("\n".join(parts) + "\n", encoding="utf-8")
    print("[ok] wrote %s (%d bytes, %d files)" % (out_path, out_path.stat().st_size, len(MANIFEST)))


if __name__ == "__main__":
    main()
